package com.ponysdk.core.ui.list.refreshable;

import com.ponysdk.core.ui.basic.IsPWidget;

public class Cell<D, W extends IsPWidget> {

    private W widget;
    private D data;
    private int row;
    private int col;

    public W getWidget() {
        return widget;
    }

    public void setWidget(final W widget) {
        this.widget = widget;
    }

    public D getData() {
        return data;
    }

    public void setData(final D data) {
        this.data = data;
    }

    public int getRow() {
        return row;
    }

    public void setRow(final int row) {
        this.row = row;
    }

    public int getCol() {
        return col;
    }

    public void setCol(final int col) {
        this.col = col;
    }
}